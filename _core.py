    def start(self):
        for frame in self.camera.start_flow():
            res = self.model(frame)
            img = res[0].plot()

            target = 0
            cropped_img = []
            quantity = 0
            for box in res[0].boxes:
                if int(box.cls[0]) == target and box.conf[0] > 0.8:
                    quantity+=1
                    x1, y1, x2, y2 = map(int, box.xyxy[0])
                    cropped = img[y1:y2, x1:x2]
                    cropped_img.append(cropped)
            if quantity >= self.mode * 0.85:
                cols = 2
                rows = (len(cropped_img) + cols - 1) // cols

                c,axes = plt.subplots(rows,cols, figsize=(12, 4))
                namesl = {}
                for i,cropped in enumerate(cropped_img):
                    axes[i].imshow(cropped)
                    axes[i].axis('off')
                    name = random.choice(names)
                    namesl[np.array2string(cropped)] = name
