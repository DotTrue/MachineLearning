                cv2.destroyAllWindows()

                winsound.Beep(frequency=2000,duration=500)

                c, axes = plt.subplots(rows, cols, figsize=(12, 4))
                random.shuffle(cropped_img)
                random.shuffle(cropped_img)
                random.shuffle(cropped_img)

                for i in range(self.choice):
                    c = cropped_img.pop()
                    axes[i].imshow(c)
                    axes[i].axis('off')
                    axes[i].set_title(f"Везунчик №{i+1}: {namesl[np.array2string(c)]}")
